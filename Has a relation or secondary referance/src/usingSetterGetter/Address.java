package usingSetterGetter;

public class Address 
{
	private String city;
	private String area;
	
	public void setCity(String city)
	{
		this.city = city;
	}
	public String getCity()
	{
		return city;
	}
	
	public void setArea(String area)
	{
		this.area = area;
	}
	public String getArea()
	{
		return area;
	}
}
